# Precalculate fuel mass flow points directly from BSFC to avoid interpolation artifacts around zero torque
brake_power_kw = (brake_torque_nm * engine_speeds * 2 * np.pi) / 60000.0
fuel_mass_flow_kg_s = (bsfc_g_kwh * brake_power_kw) / 3.6e6
fuel_mass_flow_kg_s = np.maximum(0.0, fuel_mass_flow_kg_s)

# Conversion Factor calculation
def calculate_losses_torque():
    # T_indicated = (IMEP * Volume) / (4 * pi) for 4-stroke
    # IMEP in bar, so multiply by 10^5
    t_ind = (imep_bar * 1e5 * VOLUME_DISPLACEMENT) / (4 * np.pi)
    t_losses = t_ind - brake_torque_nm
    return t_losses

t_losses_nm = calculate_losses_torque()

def get_engine_metrics(rpm):
    """
    Returns fuel flow rate (kg/s) and available brake torque (Nm) for a given RPM.
    """
    rpm = max(100, min(6100, rpm))
    
    t_brake = np.interp(rpm, engine_speeds, brake_torque_nm)
    m_dot = np.interp(rpm, engine_speeds, fuel_mass_flow_kg_s)
    
    return m_dot, t_brake

def get_engine_braking_torque(rpm):
    """
    Returns the drag torque (Nm) when the engine is backdriven (off).
    """
    rpm = max(100, min(6100, rpm))
    return np.interp(rpm, engine_speeds, t_losses_nm)
